#include <stdlib.h>
#include <stdio.h>

/**
 * démarre le processus 1 puis attends 10sec
 * démarre le processus 3  puis attends 5 sec
 * démarre le processus 5 puis affiche terminé sur le terminal
 *
 * @return     0 
 */
int main() {
  system("echo \"processus 1 \""); 
  system("sleep 10"); // processus 2
  system("echo \"processus 3 \""); 
  system("sleep 5"); // processus 4
  system("echo \"processus 5 \""); 

  printf("\n terminé \n");
  return 0;
}
